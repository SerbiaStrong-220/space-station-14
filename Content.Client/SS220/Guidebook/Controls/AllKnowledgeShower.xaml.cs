using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;

namespace Content.Client.SS220.Guidebook.Controls;

[UsedImplicitly, GenerateTypedNameReferences]
public sealed partial class AllKnowledgeShower : BoxContainer, IDocumentTag
{
    [Dependency] private readonly IPrototypeManager _prototype = default!;

    public AllKnowledgeShower()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        foreach (var knowledge in _prototype.EnumeratePrototypes<KnowledgePrototype>().OrderBy(x => Loc.GetString(x.KnowledgeName)))
        {
            var knowledgeLabel = new KnowledgeLabel();

            LnowledgesContainer.AddChild(knowledgeLabel);

            knowledgeLabel.SetKnowledge(knowledge);
        }
    }

    public bool TryParseTag(Dictionary<string, string> args, [NotNullWhen(true)] out Control? control)
    {
        control = this;
        return true;
    }
}
