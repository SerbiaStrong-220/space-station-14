using Content.Client.Computer;
using Content.Client.UserInterface.Controls;
using Content.Shared.SS220.Shuttles.BUIStates;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Localization;

namespace Content.Client.SS220.Shuttles.UI;

[GenerateTypedNameReferences]
public sealed partial class SS220StealthIFFConsoleWindow : FancyWindow,
    IComputerWindow<SS220StealthIFFConsoleBoundUserInterfaceState>
{
    public event Action? ActivateStealth;

    public SS220StealthIFFConsoleWindow()
    {
        RobustXamlLoader.Load(this);
        StealthButton.OnPressed += _ => ActivateStealth?.Invoke();
        UpdateLabels(TimeSpan.Zero, TimeSpan.Zero);
    }

    public void UpdateState(SS220StealthIFFConsoleBoundUserInterfaceState state)
    {
        UpdateLabels(state.StealthDuration, state.Cooldown);

        if (state.StealthDuration > TimeSpan.Zero || state.Cooldown > TimeSpan.Zero)
        {
            StealthButton.Disabled = true;
            StealthButton.Text = state.StealthDuration > TimeSpan.Zero
                ? Loc.GetString("ss220-stealth-iff-console-active")
                : Loc.GetString("ss220-stealth-iff-console-cooldown");
        }
        else
        {
            StealthButton.Disabled = false;
            StealthButton.Text = Loc.GetString("ss220-stealth-iff-console-activate");
        }
    }

    private void UpdateLabels(TimeSpan stealthDuration, TimeSpan cooldown)
    {
        StealthTimerLabel.Text = Loc.GetString("ss220-stealth-iff-console-time", ("seconds", $"{stealthDuration.TotalSeconds:F0}"));
        CooldownTimerLabel.Text = Loc.GetString("ss220-stealth-iff-console-cooldown-time", ("seconds", $"{cooldown.TotalSeconds:F0}"));
    }
}
