using Content.Client.Stylesheets;
using Content.Client.UserInterface.Controls;
using Content.Shared.SS220.Economy;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.SS220.Economy;

[GenerateTypedNameReferences]
public sealed partial class EconomyEFTPOSWindow : FancyWindow
{
    public event Action? OnPaymentButtonPressed;
    public event Action? OnPrintReceiptButtonPressed;
    public event Action<int>? OnKeypadButtonPressed;
    public event Action? OnClearButtonPressed;
    public event Action? OnEnterButtonPressed;
    public event Action<int>? OnCardLockButtonPressed;

    public EconomyEFTPOSWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        FillKeypadGrid();

        PaymentButton.OnPressed += _ =>
        {
            OnPaymentButtonPressed?.Invoke();
        };

        PrintReceiptButton.OnPressed += _ =>
        {
            OnPrintReceiptButtonPressed?.Invoke();
        };

        AmountLineEdit.OnTextChanged += _ =>
        {
            if (!int.TryParse(AmountLineEdit.Text, out var result) || result <= 0)
            {
                AmountLineEdit.Text = string.Empty;
            }
        };

        CardLockButton.OnPressed += _ =>
        {
            if (!int.TryParse(AmountLineEdit.Text, out var result) || result < 0)
                result = 0;

            OnCardLockButtonPressed?.Invoke(result);
        };
    }

    /// <summary>
    ///     Fill keypad buttons in keypad grid.
    /// </summary>
    private void FillKeypadGrid()
    {
        // add 3 rows of keypad buttons (1-9)
        for (var i = 1; i <= 9; i++)
        {
            AddKeypadButton(i);
        }

        // clear button
        var clearBtn = new Button
        {
            Text = "C",
            StyleClasses = { StyleBase.ButtonCaution }
        };
        clearBtn.OnPressed += _ => OnClearButtonPressed?.Invoke();
        KeypadGrid.AddChild(clearBtn);

        // zero button
        AddKeypadButton(0);

        // enter button
        var enterBtn = new Button
        {
            Text = "E"
        };
        enterBtn.OnPressed += _ => OnEnterButtonPressed?.Invoke();
        KeypadGrid.AddChild(enterBtn);
    }

    private void AddKeypadButton(int i)
    {
        var btn = new Button()
        {
            Text = i.ToString()
        };

        btn.OnPressed += _ => OnKeypadButtonPressed?.Invoke(i);
        KeypadGrid.AddChild(btn);
    }
    public void UpdateState(BoundUserInterfaceState state)
    {
        if (state is not EconomyEFTPOSUiState eftposState)
            return;

        var amountString = eftposState.Amount == default ? string.Empty : eftposState.Amount.ToString();
        AmountLineEdit.Text = amountString;

        if (eftposState.Amount == default)
        {
            AmountToPayLabel.Text = Loc.GetString("economy-eftpos-ui-amount-default");
            AmountToPay.Text = string.Empty;
            PaymentControls.Visible = false;
        }
        else
        {
            AmountToPayLabel.Text = Loc.GetString("economy-eftpos-ui-amount-label");
            AmountToPay.Text = Loc.GetString("economy-eftpos-ui-amount-text", ("amount", amountString));
            PaymentControls.Visible = true;
        }

        if (eftposState.PayerBankAccountId == default)
            PaymentButton.Text = Loc.GetString("economy-eftpos-ui-payment-await-card-text");
        else
            PaymentButton.Text = Loc.GetString("economy-eftpos-ui-payment-await-pin-text");

        PayerPinInputText.Text = VisualizePinCode(eftposState.PayerPinInput.Length);

        if (eftposState.PrintReceipt)
            PrintReceiptButton.Text = Loc.GetString("economy-eftpos-ui-receipt-print-yes");
        else
            PrintReceiptButton.Text = Loc.GetString("economy-eftpos-ui-receipt-print-no");

        PrintReceiptButton.Pressed = eftposState.PrintReceipt;

        if (eftposState.Locked)
        {
            CardLockButton.Text = Loc.GetString("economy-eftpos-ui-card-unlock-text");
            CardLockButton.ToolTip = Loc.GetString("economy-eftpos-ui-card-unlock-desc");
            AmountLineEdit.Editable = false;
        }
        else
        {
            CardLockButton.Text = Loc.GetString("economy-eftpos-ui-card-lock-text");
            CardLockButton.ToolTip = Loc.GetString("economy-eftpos-ui-card-lock-desc");
            AmountLineEdit.Editable = true;
        }

        AccountIdText.Text = eftposState.OwnerBankAccountId == default
            ? Loc.GetString("economy-eftpos-ui-account-id-empty")
            : eftposState.OwnerBankAccountId.ToString();

        AccountNameLabel.Text = eftposState.OwnerName == string.Empty
            ? string.Empty
            : Loc.GetString("economy-eftpos-ui-account-name-text", ("ownerName", eftposState.OwnerName));
    }

    private static string VisualizePinCode(int codeLength)
    {
        var code = new string('*', codeLength);
        var blanksCount = SharedEconomyBankCardSystem.PinCodeLength - codeLength;
        var blanks = new string('_', blanksCount);
        return code + blanks;
    }
}
