using Content.Client.Stylesheets;
using Content.Client.UserInterface.Controls;
using Content.Shared.SS220.Economy;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.SS220.Economy;

[GenerateTypedNameReferences]
public sealed partial class EconomyATMWindow : FancyWindow
{
    public event Action<int>? OnKeypadButtonPressed;
    public event Action? OnClearButtonPressed;
    public event Action<int>? OnEnterButtonPressed;
    public event Action? OnLinkButtonPressed;
    public event Action? OnCreateButtonPressed;

    public EconomyATMWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        FillKeypadGrid();

        LinkButton.OnPressed += _ =>
        {
            OnLinkButtonPressed?.Invoke();
        };

        CreateButton.OnPressed += _ =>
        {
            OnCreateButtonPressed?.Invoke();
        };
    }

    /// <summary>
    ///     Fill keypad buttons in keypad grid.
    /// </summary>
    private void FillKeypadGrid()
    {
        // add 3 rows of keypad buttons (1-9)
        for (var i = 1; i <= 9; i++)
        {
            AddKeypadButton(i);
        }

        // clear button
        var clearBtn = new Button
        {
            Text = "C",
            StyleClasses = { StyleBase.ButtonCaution }
        };
        clearBtn.OnPressed += _ => OnClearButtonPressed?.Invoke();
        KeypadGrid.AddChild(clearBtn);

        // zero button
        AddKeypadButton(0);

        // enter button
        var enterBtn = new Button
        {
            Text = "E"
        };
        enterBtn.OnPressed += _ => OnEnterButtonPressed?.Invoke(WithdrawSlider.Value);
        KeypadGrid.AddChild(enterBtn);
    }

    private void AddKeypadButton(int i)
    {
        var btn = new Button()
        {
            Text = i.ToString()
        };

        btn.OnPressed += _ => OnKeypadButtonPressed?.Invoke(i);
        KeypadGrid.AddChild(btn);
    }

    public void UpdateState(BoundUserInterfaceState state)
    {
        if (state is not EconomyATMUiState atmState)
            return;

        if (atmState.Emagged)
        {
            FlavorLeft.Text = Loc.GetString("economy-atm-ui-window-flavor-emagged-left");
            NTLogo.Visible = false;
        }
        else
        {
            FlavorLeft.Text = Loc.GetString("economy-atm-ui-window-flavor-left");
            NTLogo.Visible = true;
        }

        StatusLabel.Text = atmState.InfoMessage;

        AccountDataBox.Visible = false;
        Divider.Visible = false;
        WithdrawSlider.Visible = false;
        PinInputBox.Visible = false;
        InvalidCardOptionsBox.Visible = false;

        switch (atmState.CardState)
        {
            case CardStateEnum.Default:
                break;
            case CardStateEnum.Absent:
                break;
            case CardStateEnum.Invalid:
                InvalidCardOptionsBox.Visible = true;
                LinkButton.Visible = !atmState.UnemployedAlert;
                CreateButton.Visible = atmState.UnemployedAlert;
                break;
            case CardStateEnum.Valid:

                PinInputText.Text = VisualizePinCode(atmState.PinInput.Length);
                AccountIdLabel.Text = Loc.GetString("economy-atm-ui-account-id-text", ("accountId", atmState.BankAccount.AccountId));
                AccountOwnerNameLabel.Text = atmState.BankAccount.AccountOwnerName == string.Empty
                    ? string.Empty
                    : Loc.GetString("economy-atm-ui-account-name-text", ("name", atmState.BankAccount.AccountOwnerName));
                BalanceLabel.Text = Loc.GetString("economy-atm-ui-account-balance", ("balance", atmState.BankAccount.Balance));
                AccountDataBox.Visible = true;

                if (atmState.BankAccount.Balance > 0)
                {
                    Divider.Visible = true;
                    WithdrawSlider.Visible = true;
                    PinInputBox.Visible = true;
                    WithdrawSlider.MaxValue = atmState.BankAccount.Balance;
                    WithdrawSlider.Value = Math.Min(WithdrawSlider.Value, atmState.BankAccount.Balance);
                }
                else
                {
                    StatusLabel.Text = string.Empty;
                }
                break;
        }
    }

    private static string VisualizePinCode(int codeLength)
    {
        var code = new string('*', codeLength);
        var blanksCount = SharedEconomyBankCardSystem.PinCodeLength - codeLength;
        var blanks = new string('_', blanksCount);
        return code + blanks;
    }
}
