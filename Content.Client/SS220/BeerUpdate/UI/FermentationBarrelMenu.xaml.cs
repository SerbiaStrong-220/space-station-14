using Content.Shared.Chemistry.Reagent;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;
using Content.Client.UserInterface.Controls;
using Robust.Shared.Prototypes;

namespace Content.Client.SS220.BeerUpdate.UI;

[GenerateTypedNameReferences]
public sealed partial class FermentationBarrelMenu : FancyWindow
{
    public event Action? OnStartStop;
    public event Action? OnModeChange;

    private readonly IPrototypeManager _prototypeManager = IoCManager.Resolve<IPrototypeManager>();

    public FermentationBarrelMenu()
    {
        RobustXamlLoader.Load(this);

        StartStopButton.OnPressed += _ => OnStartStop?.Invoke();
        ModeChangeButton.OnPressed += _ => OnModeChange?.Invoke();
    }

    public void UpdateState(bool isActive, float elapsedTime, ReagentQuantity[]? reagents = null)
    {
        StatusLabel.Text = Loc.GetString(isActive ? "fermentation-barrel-menu-status-active" : "fermentation-barrel-menu-status-idle");

        var span = TimeSpan.FromSeconds(elapsedTime);
        TimerLabel.Text = $"{(int)span.TotalMinutes:D2}:{span.Seconds:D2}";

        StartStopButton.Text = Loc.GetString(isActive ? "fermentation-barrel-menu-stop-button" : "fermentation-barrel-menu-start-button");

        ModeChangeButton.Disabled = isActive;
        UpdateReagentList(reagents);
    }

    private void UpdateReagentList(ReagentQuantity[]? reagents)
    {
        ReagentList.RemoveAllChildren();

        if (reagents == null || reagents.Length == 0)
        {
            ReagentList.AddChild(new Label { Text = Loc.GetString("fermentation-barrel-menu-reagents-empty") });
            return;
        }

        foreach (var reagent in reagents)
        {
            var reagentName = reagent.Reagent.ToString();
            if (_prototypeManager.TryIndex<ReagentPrototype>(reagent.Reagent.Prototype, out var proto))
            {
                reagentName = proto.LocalizedName;
            }

            var container = new BoxContainer { Orientation = BoxContainer.LayoutOrientation.Horizontal, Margin = new Thickness(0, 2, 0, 2) };
            var reagentInfo = $"{reagentName}: {reagent.Quantity}u";
            container.AddChild(new Label { Text = reagentInfo });
            ReagentList.AddChild(container);
        }
    }
}
