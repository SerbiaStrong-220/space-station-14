using Content.Shared.Objectives.Systems;
using JetBrains.Annotations;
using Robust.Client.AutoGenerated;
using Robust.Client.Console;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Player;

namespace Content.Client.SS220.Administration.UI.Tabs.AdminTab;

[GenerateTypedNameReferences]
[UsedImplicitly]
public sealed partial class AddObjectiveWindow : DefaultWindow
{
    [Dependency] private IEntityManager _entMan = default!;
    [Dependency] private IClientConsoleHost _clientConsoleHost = default!;

    private ICommonSession? _selectedAntagonist;
    private bool _customObjective;

    public AddObjectiveWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        var objective = _entMan.System<SharedObjectivesSystem>();

        if (objective.ObjectivesQuery == null)
            return;

        ObjectiveOptionButton.AddItem(Loc.GetString("ui-add-objective-option-button-placeholder"), 0);

        var index = 1;

        foreach (var objId in objective.ObjectivesQuery)
        {
            ObjectiveOptionButton.AddItem(objId, index);
            ObjectiveOptionButton.SetItemMetadata(index, objId);
            index++;
        }
    }

    protected override void EnteredTree()
    {
        base.EnteredTree();
        ApplyButton.OnPressed += ApplyButtonOnPressed;
        ForceAssignButton.OnPressed += ForceAssignButtonOnPressed;

        ObjectiveOptionButton.OnItemSelected += ObjectiveOptionButtonOnItemSelected;

        EntityIdLineEdit.OnTextChanged += InputOnTextChanged;
        CustomObjectiveNameLineEdit.OnTextChanged += InputOnTextChanged;
        CustomObjectiveDescLineEdit.OnTextChanged += InputOnTextChanged;
        CustomObjectiveIconLineEdit.OnTextChanged += InputOnTextChanged;
        CustomObjectiveIssuerLineEdit.OnTextChanged += InputOnTextChanged;
    }

    protected override void ExitedTree()
    {
        base.ExitedTree();
        ApplyButton.OnPressed -= ApplyButtonOnPressed;
        ForceAssignButton.OnPressed -= ForceAssignButtonOnPressed;

        ObjectiveOptionButton.OnItemSelected -= ObjectiveOptionButtonOnItemSelected;

        EntityIdLineEdit.OnTextChanged -= InputOnTextChanged;
        CustomObjectiveNameLineEdit.OnTextChanged -= InputOnTextChanged;
        CustomObjectiveDescLineEdit.OnTextChanged -= InputOnTextChanged;
        CustomObjectiveIconLineEdit.OnTextChanged -= InputOnTextChanged;
        CustomObjectiveIssuerLineEdit.OnTextChanged -= InputOnTextChanged;
    }

    private void UpdateInputStates()
    {
        var hasUsualText = ObjectiveOptionButton.SelectedMetadata != null ||
                           !string.IsNullOrWhiteSpace(EntityIdLineEdit.Text);

        var hasCustomText = !string.IsNullOrWhiteSpace(CustomObjectiveNameLineEdit.Text) ||
                            !string.IsNullOrWhiteSpace(CustomObjectiveDescLineEdit.Text) ||
                            !string.IsNullOrWhiteSpace(CustomObjectiveIconLineEdit.Text) ||
                            !string.IsNullOrWhiteSpace(CustomObjectiveIssuerLineEdit.Text);

        UsualObjectiveContainer.Visible = !hasCustomText;
        CustomObjectiveContainer.Visible = !hasUsualText;

        _customObjective = hasCustomText;
    }

    public void SetAntagonist(ICommonSession antagonistPlayerName)
    {
        _selectedAntagonist = antagonistPlayerName;
    }

    private void ApplyButtonOnPressed(BaseButton.ButtonEventArgs _)
    {
        OnApplyButtonPressed(false);
    }

    private void ForceAssignButtonOnPressed(BaseButton.ButtonEventArgs _)
    {
        OnApplyButtonPressed(true);
    }

    private void ObjectiveOptionButtonOnItemSelected(OptionButton.ItemSelectedEventArgs args)
    {
        ObjectiveOptionButton.SelectId(args.Id);
        UpdateInputStates();
    }

    private void InputOnTextChanged(LineEdit.LineEditEventArgs _)
    {
        UpdateInputStates();
    }

    private void OnApplyButtonPressed(bool force)
    {
        if (_selectedAntagonist == null)
            return;

        if (_customObjective)
        {
            var name = CustomObjectiveNameLineEdit.Text;
            if (string.IsNullOrWhiteSpace(name))
                return;

            var desc = CustomObjectiveDescLineEdit.Text;
            var icon = CustomObjectiveIconLineEdit.Text;
            var issuer = CustomObjectiveIssuerLineEdit.Text;

            _clientConsoleHost.ExecuteCommand($"addcustomobjective {_selectedAntagonist.Name} \"{name}\" \"{desc}\" \"{icon}\" \"{issuer}\"");
        }
        else
        {
            var objectiveId = ObjectiveOptionButton.SelectedMetadata as string;
            var entityId = EntityIdLineEdit.Text;
            if (string.IsNullOrEmpty(objectiveId) && string.IsNullOrWhiteSpace(entityId))
                return;

            _clientConsoleHost.ExecuteCommand($"addobjective {_selectedAntagonist.Name} \"{objectiveId}\" \"{entityId}\" {force}");
        }
    }
}
