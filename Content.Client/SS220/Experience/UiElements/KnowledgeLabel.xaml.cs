using Content.Client.Message;
using Content.Client.SS220.Experience.Ui;
using Content.Shared.SS220.Experience;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;
using Robust.Shared.Utility;

namespace Content.Client.SS220.Experience.UiElements;

[GenerateTypedNameReferences]
public sealed partial class KnowledgeLabel : BoxContainer
{
    [Dependency] private readonly IPrototypeManager _prototype = default!;

    public KnowledgeLabel()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
    }

    public void SetKnowledge(ProtoId<KnowledgePrototype> knowledge)
    {
        if (!_prototype.Resolve(knowledge, out var knowledgePrototype))
            return;

        KnowledgeName.SetMarkup(Loc.GetString(knowledgePrototype.KnowledgeName));

        KnowledgeName.TooltipSupplier = ExperienceUiStyleDefinitions.RichExperienceTooltip;
        KnowledgeName.ToolTip = Loc.GetString(knowledgePrototype.KnowledgeDescription);
    }

}
