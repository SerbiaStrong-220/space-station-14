using Content.Shared.SS220.Experience;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;

namespace Content.Client.SS220.Experience.UiElements;

[GenerateTypedNameReferences]
public sealed partial class KnowledgeLabel : BoxContainer
{
    [Dependency] private readonly IPrototypeManager _prototype = default!;

    public KnowledgeLabel()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
    }

    public void SetKnowledge(ProtoId<KnowledgePrototype> knowledge)
    {
        if (!_prototype.Resolve(knowledge, out var knowledgePrototype))
            return;

        KnowledgeName.SetMessage(knowledgePrototype.KnowledgeName);
        KnowledgeDescription.SetMessage(knowledgePrototype.KnowledgeDescription);
    }
}
