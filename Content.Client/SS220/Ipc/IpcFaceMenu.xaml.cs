using System;
using Content.Shared.SS220.Ipc;
using Content.Shared.Humanoid.Markings;
using Content.Client.UserInterface.Controls;
using Robust.Client.GameObjects;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.GameObjects;
using Robust.Shared.IoC;
using Robust.Shared.Localization;
using Robust.Shared.Prototypes;
using Robust.Shared.Utility;
using System.Numerics;

namespace Content.Client.SS220.Ipc;

[GenerateTypedNameReferences]
public sealed partial class IpcFaceMenu : FancyWindow
{
    [Dependency] private readonly IPrototypeManager _prototype = default!;
    [Dependency] private readonly IEntityManager _entMan = default!;
    private readonly SpriteSystem _sprite;
    private string _currentCategory = "all";
    public event Action<string>? FaceSelected;

    public IpcFaceMenu()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
        
        _sprite = _entMan.System<SpriteSystem>();
    }

    public void Populate(string profileId, string selected)
    {
        if (!_prototype.TryIndex<IpcFaceProfilePrototype>(profileId, out var profile))
            return;

        CategoryList.RemoveAllChildren();

        var categories = new HashSet<string> { "all" };
        
        foreach (var entry in profile.Faces)
        {
            if (!string.IsNullOrEmpty(entry.Category))
                categories.Add(entry.Category);
        }

        foreach (var catId in categories)
        {
            var catName = Loc.GetString($"ipc-cat-{catId}");
            var catButton = new Button 
            { 
                Text = catName, 
                ToggleMode = true, 
                Pressed = (_currentCategory == catId),
                Margin = new Thickness(0, 0, 0, 4)
            };
            
            catButton.OnPressed += _ => 
            {
                _currentCategory = catId;
                Populate(profileId, selected);
            };
            CategoryList.AddChild(catButton);
        }

        OptionsContainer.RemoveAllChildren();

        foreach (var entry in profile.Faces)
        {
            if (_currentCategory != "all" && entry.Category != _currentCategory) 
                continue;

            if (!_prototype.TryIndex(entry.Id, out MarkingPrototype? marking))
                continue;

            var button = new Button
            {
                HorizontalExpand = true,
                Margin = new Thickness(0, 0, 0, 4),
                ToggleMode = true,
                Pressed = (marking.ID == selected)
            };

            var content = new BoxContainer { Orientation = BoxContainer.LayoutOrientation.Horizontal, HorizontalExpand = true };
            
            content.AddChild(new Label 
            { 
                Text = Loc.GetString($"marking-{marking.ID}"), 
                HorizontalExpand = true,
                Margin = new Thickness(5, 0),
            });

            if (marking.Sprites.Count > 0)
            {
                var texture = _sprite.Frame0(marking.Sprites[0]); 
                content.AddChild(new TextureRect
                {
                    Texture = texture,
                    //TextureScale = new Vector2(5.5f, 5.5f),
                    Stretch = TextureRect.StretchMode.KeepAspectCentered,
                    SetSize = new Vector2(32, 32)
                });
            }

            button.AddChild(content);
            button.OnPressed += _ => {
                FaceSelected?.Invoke(marking.ID);
                Populate(profileId, marking.ID);
            };

            OptionsContainer.AddChild(button);
        }
    }
}